package com.capgemini.um.beans;

public class MacBean {

	private String macEmail;
	private String macPassword;

	// Constructor
	public MacBean() {

	}

	// Getter and setter
	public String getMacEmail() {
		return macEmail;
	}

	public void setMacEmail(String macEmail) {
		this.macEmail = macEmail;
	}

	public String getMacPassword() {
		return macPassword;
	}

	public void setMacPassword(String macPassword) {
		this.macPassword = macPassword;
	}

}// End of class
